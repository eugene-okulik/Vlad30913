def extract_and_add_10(result_str):
    # Разделяем строку на части по разделителю ':' с помощью метода split
    parts = result_str.split(':')
    # Извлекаем вторую часть строки (после двоеточия), убираем пробелы с помощью strip и конвертируем её в целое число
    number = int(parts[1].strip())
    # Прибавляем 10 к числу и возвращаем результат
    return number + 10

results = [
    "результат операции: 42",
    "результат операции: 54",
    "результат работы программы: 209",
    "результат: 2"
]

for result in results:
    print(extract_and_add_10(result))
